package calculator;

/**
 * A custom exception class to be used for incorrect data types.
 * 
 * @author ZHAC004
 */
public class BadTypeException extends Exception {

  private static final long serialVersionUID = 6265411551564893049L;

  /**
   * A constructor for BadTypeException which takes a string.
   *
   * @param message to output if the exception is called
   */
  public BadTypeException(String message) {
    super(message);
  }

}
