package calculator;

/**
 * Enum to store different symbol inputs for the calculator.
 * 
 * @author ZHAC004
 */
public enum Symbol { // The constants associated with the enum are the only accepted symbols

  LEFT_BRACKET("("),
  RIGHT_BRACKET(")"),
  TIMES("*"),
  DIVIDE("/"),
  PLUS("+"),
  MINUS("-"),
  INVALID("Invalid expression!");

  private String symbol; // symbol is the string inside the parentheses of the constants

  /**
   * Private constructor to take the associated string for the constant.
   *
   * @param symbol string associated with the constant within the class
   */
  private Symbol(String symbol) {
    this.symbol = symbol; 
  }

  
  /**
   * Returns constant as a string output.
   */
  public final String toString() {
    return symbol;
  }
}
